package com.mobileapp.POJO;

public class ServiceList_POJO {

    String ID = "";
    String Hostname = "";
    String MainIPP = "";
    String Power;

    public String getID() {
        return ID;
    }

    public void setID(String ID) {
        this.ID = ID;
    }

    public String getHostname() {
        return Hostname;
    }

    public void setHostname(String hostname) {
        Hostname = hostname;
    }

    public String getMainIPP() {
        return MainIPP;
    }

    public void setMainIPP(String mainIPP) {
        MainIPP = mainIPP;
    }

    public String getPower() {
        return Power;
    }

    public void setPower(String power) {
        Power = power;
    }
}
